/* Character Constant - 문자 상수 */
#include <stdio.h>

int main()
{
        printf("---- character ----\n");
        printf("100:\t%c\n", 100);
        printf("70:\t%c\n", 70);
        printf("---- integer ----\n");
        printf("'A':\t%d\n", 'A');
        printf("'Z':\t%d\n", 'Z');
        printf("'a':\t%d\n", 'a');
        printf("'z':\t%d\n", 'z');
}
