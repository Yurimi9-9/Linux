/* Character Variable */
#include <stdio.h>

int main()
{
        char ch1, ch2; 

        ch1 = 'a';    // 문자 'a' 저장
        ch2 = 98;     // 문자 'b'의 정수값인 98 저장

        printf("ch1 : %c   %d\n", ch1, ch1);
        printf("ch2 : %c   %d\n", ch2, ch2);
}

/*
*   [ 출력 ]
*   ch1 : a    97
*   ch2 : b    98
*/
